package com.qa.TestNG.com.qa.TestNG;

public final class FinalConcept {
	//final keyword is always used to prevent inheritance
	//final keyword makes the values constant and can't be changed.
	//It can used in the class level as well.
	//If you declare a class as a final,you won't be able to inherit that class.
	//If you declare a method as final,you won't be able to override that method.
	public static void main(String args[])
	{
		final int i=10;
	
	}
	
	public void start()
	{
		System.out.println("this is the start method");
	}
}
